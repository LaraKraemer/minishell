char	*ft_strjoin2(const char *s1, const char *s2)
{
    char	*result;
    size_t	i;
    size_t	j;

    i = ft_strlen(s1);
    j = ft_strlen(s2);
    result = malloc(i + j + 1);
    if (!result)
        return (NULL);
    i = 0;
	j = 0;
    while (s1[i])
        result[j++] = s1[i++];
    i = 0;
    while (s2[i])
        result[j++] = s2[i++];
    result[j] = '\0';
    return (result);
}
